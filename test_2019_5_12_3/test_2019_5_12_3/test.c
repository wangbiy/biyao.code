//1.调整数组使奇数全部都位于偶数前面。
//题目：输入一个整数数组，实现一个函数，
//来调整该数组中数字的顺序使得数组中所有的奇数位于数组的前半部分，
//所有偶数位于数组的后半部分。
#define _CRT_SECURE_NO_WARNINGS 1
#include <stdio.h>
#include <stdlib.h>
void Rank(int arr[], int sz)
{
	int tmp = 0;
	int left = 0;
	int right = sz - 1;
	while (left < right)
	{
		if (arr[left] % 2 == 0)
		{
			tmp = arr[left];
			arr[left] = arr[right];
			arr[right] = tmp;
			right--;
		}
		else
		{
			left++;
		}
	}
}
int main()
{
	int arr[] = { 4, 7, 18, 17, 13, 2, 14, 45, 6 };
	int sz = sizeof(arr) / sizeof(arr[0]);
	int i = 0;
	Rank(arr, sz);
	for (i = 0; i < sz; i++)
	{
		printf("%d ",arr[i]);
	}
	printf("\n");
	system("pause");
	return 0;
}